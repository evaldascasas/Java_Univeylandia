    private void plenarTaula() {
        //estructuraDades.arrayClient
        DefaultTableModel taula = new DefaultTableModel();
        taula.addColumn("Habitació");  //Dono nom a les columnes
        taula.addColumn("Tipus habitació"); //Dono nom a les columnes
        taula.addColumn("Nº llits"); //Dono nom a les columnes
        taula.addColumn("Tipus llits"); //Dono nom a les columnes
        taula.addColumn("Estat"); //Dono nom a les columnes

        taula.setRowCount(EstructuraDades.arrayHabitacio.size());

        int i = 0;
        for (Habitacio obj : EstructuraDades.arrayHabitacio) {
            taula.setValueAt(obj.getNumHabitacio(), i, 0);
            taula.setValueAt(obj.getTipusHabitacio(), i, 1);
            taula.setValueAt(obj.getNumLlits(), i, 2);
            taula.setValueAt(obj.getTipusLlits(), i, 3);
            taula.setValueAt(obj.getEstatHabitacio(), i, 4);

            i++;
        }
        jTable1.setModel(taula);
    }
