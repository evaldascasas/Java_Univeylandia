    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        int input = JOptionPane.showConfirmDialog(null, "Estas segur que vols seleccionar aquesta habitació?");
        // 0=yes, 1=no, 2=cancel
        if (input == 0) {
            String numHab = jTable1.getValueAt(index, 0).toString();
            String tiphab = jTable1.getValueAt(index, 1).toString();
            int numLlit = Integer.parseInt(jTable1.getValueAt(index, 2).toString());
            String tipusLlit = jTable1.getValueAt(index, 3).toString();
            String estat = jTable1.getValueAt(index, 4).toString();

            Moduls.InserirReservaHabitacio.InserirHabit(numHab, tiphab, numLlit, tipusLlit,estat);
            JOptionPane.showMessageDialog(this, "Habitació seleccionada satisfactoriament");
            plenarTaula();
            JFrameInserirReservaHabitacio inserir = new JFrameInserirReservaHabitacio();
            inserir.setVisible(true);
            dispose();
        } else if (input == 1) {
            JOptionPane.showMessageDialog(this, "Habitació no seleccionada");

        } else {
            JFrameMenuReservaHabitacio menu = new JFrameMenuReservaHabitacio();
            menu.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

