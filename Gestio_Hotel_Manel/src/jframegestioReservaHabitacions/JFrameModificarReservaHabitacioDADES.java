    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        String DNI = jTextField1.getText();
        String numHab = jTextField2.getText();      
        String dataIn = jTextField3.getText();
        String dataOut = jTextField4.getText();
        String estat = jTextField7.getText();
        
        int input = JOptionPane.showConfirmDialog(null, "Estas segur que vols modificar la reserva?");
        // 0=yes, 1=no, 2=cancel
        if (input == 0) {
            Moduls.ModificarReservaHabitacio.modificarDadesReservaHabitacio(classesPrincipals.EstructuraDades.arrayReservaHab, DNI, numHab,dataIn,dataOut,estat);
            JOptionPane.showMessageDialog(this, "Reserva modificada correctament!");
        } else if (input == 1) {
            JOptionPane.showMessageDialog(this, "Reserva NO modificada");
        } else {
            JFrameMenuReservaHabitacio menu = new JFrameMenuReservaHabitacio();
            menu.setVisible(true);
            dispose();
        }
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField7.setText("");
        
    }//GEN-LAST:event_jButton3ActionPerformed

