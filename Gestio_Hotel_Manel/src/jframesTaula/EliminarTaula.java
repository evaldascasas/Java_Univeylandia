    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        int input = JOptionPane.showConfirmDialog(null, "Estas segur que vols eliminar la Taula?");
        // 0=yes, 1=no, 2=cancel
        if (input == 0) {
            String idTaula = jTable1.getValueAt(index, 0).toString();
            String numTaula = jTable1.getValueAt(index, 1).toString();
            String numCadires = jTable1.getValueAt(index, 2).toString();

            Moduls.EliminarTaula.elimTaula(classesPrincipals.EstructuraDades.arrayTaula, idTaula, numTaula, numCadires);
            JOptionPane.showMessageDialog(this, "Taula eliminada satisfactoriament");
            plenarTaula();
        } else if (input == 1) {
            JOptionPane.showMessageDialog(this, "Taula no eliminada");

        } else {
            menuTaula menu = new menuTaula();
            menu.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

